 <script>
     document.addEventListener('DOMContentLoaded', function(){
         document.getElementById('web-logo-input').addEventListener('change', function(){
             var input = this;
             uploadImage('web-logo-ctn', input);
         })
         document.getElementById('web-banner-input').addEventListener('change', function(){
             var input = this;
             uploadImage('web-banner-ctn', input);
         })
     })
   
     function uploadImage(id, input){
          if (input.files && input.files[0]){
                 var reader = new FileReader();
                 reader.onload = function(e) {
                     document.getElementById(id).innerHTML = '<img src="' + e.target.result + '" />';
                 }
   
               reader.readAsDataURL(input.files[0]);
          }
     }
   </script>
